// Object blueprints - vertex and face data from the original Lander source
// Hex values are 32-bit signed fixed-point, TILE_SIZE = 0x01000000

import { hex2float, colorFromRGB4 } from './constants.js';

function v(x, y, z) {
  return [hex2float(x), hex2float(y), hex2float(z)];
}

function f(nx, ny, nz, v1, v2, v3, color) {
  return { normal: v(nx, ny, nz), indices: [v1, v2, v3], color: colorFromRGB4(color) };
}

